namespace GenericClassLib
{
    public static class Calculator
    {
        public static int Sum(int n1, int n2) => n1 + n2;

        public static int Sub(int n1, int n2) => n1 - n2;
        public static decimal Divide(int n1, int n2) => n1 / n2;
        public static int Multiply(int n1, int n2) => n1 * n2;
    }
}
